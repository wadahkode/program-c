#include <stdio.h>

int main() {
  int a = 2;
  int b = 10;

  printf("-------------------------------\n");
  printf("Operator Artimatika dan penugasan\n");
  printf("-------------------------------\n");
  printf("Jika a = 2 dan b = 10\n");
  printf("-------------------------------\n");
  printf("Penambahan: %d\n", a + b);
  printf("Pengurangan: %d\n", b - a);
  printf("Perkalian: %d\n", a * b);
  printf("Pembagian: %d\n", b / a);
  printf("Sisa bagi: %d\n", b % a);
  printf("-------------------------------\n");

  return 0;
}